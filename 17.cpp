#include <iostream>
using namespace std;
int main()
{
    int a;
    float b;
    char c;
    double d;
    long int e;
    bool f;
    cout << "Size of int : " << sizeof(a) << " byte" << endl;
    cout << "Size of float : " << sizeof(b) << " byte" << endl;
    cout << "Size of character : " << sizeof(c) << " byte" << endl;
    cout << "Size of double : " << sizeof(d) << " byte" << endl;
    cout << "Size of long int : " << sizeof(e) << " byte" << endl;
    cout << "Size of boolean : " << sizeof(f) << " byte" << endl;
    return 0;
}
